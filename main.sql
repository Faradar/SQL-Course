DELIMITER //

CREATE FUNCTION GetCountryWithMostUsers()
RETURNS VARCHAR(255)
DETERMINISTIC
BEGIN
    DECLARE countryName VARCHAR(255);

    SELECT c.country_name
    INTO countryName
    FROM Country c
    JOIN User u ON c.country_id = u.country_id
    GROUP BY c.country_name
    ORDER BY COUNT(u.user_id) DESC
    LIMIT 1;

    RETURN countryName;
END //

DELIMITER ;

