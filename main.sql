CREATE FUNCTION GetAverageRating(animeId INT)
RETURNS DECIMAL(2, 1)
DETERMINISTIC
BEGIN
    DECLARE avgRating DECIMAL(2, 1);

    SELECT AVG(review_rating) INTO avgRating
    FROM Review
    WHERE anime_id = animeId;

    RETURN avgRating;
END $$

DELIMITER ;

