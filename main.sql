-- Create function GetNumberOfEpisodes
DELIMITER //

CREATE FUNCTION GetNumberOfEpisodes(anime_id INT)
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE episode_count INT;

    SELECT COUNT(episode_id)
    INTO episode_count
    FROM Episode
    WHERE Episode.anime_id = anime_id;

    RETURN episode_count;
END //

DELIMITER ;

